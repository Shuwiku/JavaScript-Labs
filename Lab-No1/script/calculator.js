function addition() { 
    // Запрос чисел у пользователя, преобразование в тип float
    let num1 = parseFloat(prompt('Введите первое число: '));
    let num2 = parseFloat(prompt('Введите второе число: '));
    // Вывод результата
    let output = `Результат: ${num1} + ${num2} = ${num1 + num2}`;
    alert(output);
}


